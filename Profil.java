public class Profil {

    String id;
    String nama;
    String alamat, jk;

    public String getId() {
        return id;
    }

    public void setId (String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
    
    public String getJekel() {
        return jk;
    }

    public void setJk(String jekel) {
        this.jk = jk;
    }
}
